package Loops;

public class ForLoopTable {
    public static void main(String[] args ){
        int num = 10;
        for(int i = 1; i <= 10; ++i) {
            // ++ is adding extra value.
            System.out.printf("%d * %d = %d \n", num, i, num * i);
            // %d is single integer value
        }
        }
    }


